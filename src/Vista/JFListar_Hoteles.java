    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Hotel hotel = Registro.searchHotel(this.txtCodigoBuscar.getText());
        
        if(this.txtCodigoBuscar.getText().length()==0){
            actualizarTablaHotel();
            //JOptionPane.showMessageDialog(null, "Ingrese un código para buscar");
        }
        else {
        if(hotel==null){
            JOptionPane.showMessageDialog(null, "Hotel no encontrado");
        }
        else
        {
            //JOptionPane.showMessageDialog(this, hotel.toString());
            String mascotas;
            if(hotel.getMascotas_Hotel()){
                mascotas = "Si admite";
            } else { mascotas = "No admite";}
            String matriz[] = new String[]{hotel.getCodigo_Hotel(),hotel.getNombre_Hotel(),hotel.getCiudad_Hotel(),hotel.getEstrellas_Hotel()+"",mascotas}; 
            DefaultTableModel model = (DefaultTableModel) tblResultadosHotel.getModel();
            for (int i = 0; i < model.getRowCount(); i++) {
                model.removeRow(i);
                i-=1;
            }        
            model.addRow(matriz);
        }
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

