    private void actualizarTablaHotel(){

    ArrayList<Hotel> hotelList = Registro.showAll();
    String mascotas = "";
    String matriz[][] = new String[hotelList.size()][5];
        for (int i = 0; i < hotelList.size(); i++) {
            matriz[i][0] = hotelList.get(i).getCodigo_Hotel();
            matriz[i][1] = hotelList.get(i).getNombre_Hotel();
            matriz[i][2] = hotelList.get(i).getCiudad_Hotel();
            matriz[i][3] = hotelList.get(i).getEstrellas_Hotel()+"";
            if(hotelList.get(i).getMascotas_Hotel())
            {
                mascotas = "Si admite";
            } else {
                mascotas = "No admite";
            }
            
            matriz[i][4] = mascotas;
        }tblResultadosHotel.setModel(new javax.swing.table.DefaultTableModel(matriz,new String[]{
                    "CODIGO", "NOMBRE", "CIUDAD","ESTRELLAS","MASCOTAS"
                }));
        tblResultadosHotel.setModel(new javax.swing.table.DefaultTableModel(matriz,new String[]{
                    "CODIGO", "NOMBRE", "CIUDAD","ESTRELLAS","MASCOTAS"
                }));
    }
    
    
